<p>
    Cada uno de los integrantes estará situado en el centro del grupo. Y de él salen flechas hacia los otros integrantes, que podrán ser de tres colores, según sea la relación establecida, esto es, cómo percibe esta persona a los demás integrantes.
</p>
<p>
    Si el valor promedio es inferior a 33% se establece una Relación Conflictiva (rojo); si es mayor a 33% y menor a 66% hay una relación Normal (amarillo), y si es superior a 66% hay una relación Buena (verde).
